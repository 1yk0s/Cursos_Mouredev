package strings;

public class TrimYReplace {
    public static void main(String[] args) {

        //trim y replace

        //trim => Elimina espacios en blanco al principio y final de la cadena de texto
        System.out.println(" Hola, me llamo Kevin ".trim());

        //replace => Reemplaza caracteres, informar del antiguo y del nuevo
        System.out.println(" Hola, me llamo Kevin ".replace(" ", ""));
    }
}
