package operadores;

public class OperadoresRelacionales {

    public static void main(String[] args) {

        int a = 1;
        int b = 2;

        //Operadores Relacionales
        System.out.println(a == b);
        System.out.println(a == 1);

        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a > b);
        System.out.println(a >= b);

    }

}
