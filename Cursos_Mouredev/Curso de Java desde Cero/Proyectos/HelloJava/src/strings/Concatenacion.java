package strings;

public class Concatenacion {

    public static void main(String[] args) {

        //Variables
        String name = "Kevin";
        String apellido = "Alonso";

        //Imprimir por pantalla un String
        System.out.println(name);
        //Concatenar dos Strings e imprimirlos por pantalla
        System.out.println(name + apellido);
        //Concatenar un espacio en blanco con los dos Strings
        System.out.println(name + " " + apellido);




    }
}
