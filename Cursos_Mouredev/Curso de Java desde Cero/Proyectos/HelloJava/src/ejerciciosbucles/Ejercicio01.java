/*
01.- Imprime los números del 1 al 10 usando un while
 */

package ejerciciosbucles;

public class Ejercicio01 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}

