/*
Declara una variable de tipo String con tu localidad y, a continuación,
cambia su valor y vuelve a imprimirla.
 */

package ejerciciosprincipiantes;

public class Exercise07 {
    public static void main(String[] args) {
        String localidad = "León";
        System.out.println("Mi localidad es: " + localidad);
        localidad = "Trobajo del Camino";
        System.out.println("Ahora la localidad es: " + localidad);
    }
}
